    fn linear_kf(time_ns: u64, value: f64) -> NumericKeyframe {
        NumericKeyframe {
            time_ns,
            value,
            interpolation: KeyframeInterpolation::Linear,
            bezier_controls: None,
        }
    }

    fn make_subtitle(start_ns: u64, end_ns: u64, text: &str) -> SubtitleSegment {
        SubtitleSegment {
            id: "seg-1".to_string(),
            start_ns,
            end_ns,
            text: text.to_string(),
            words: Vec::new(),
        }
    }

